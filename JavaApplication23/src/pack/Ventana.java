    private void botonComenzarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonComenzarActionPerformed
        if(evt.getSource()==botonComenzar){
        recorrido=new Hilos(panel1);
        hilo=new Thread(recorrido);
        hilo.start();
        if(hiloCorriendo==false){
            correrHilo=true;
            hiloCorriendo=true;
            iniciarHiloReloj();
        }
        
        }
    }//GEN-LAST:event_botonComenzarActionPerformed

    private void iniciarHiloReloj(){
        if(correrHilo==true){
            Reloj timer=new Reloj(tiempo);
            timer.start();  
        }
    }
    
    private void comboDificultadItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboDificultadItemStateChanged
        if(evt.getSource()==comboDificultad){
            String seleccion=comboDificultad.getSelectedItem().toString();
            switch(seleccion){
            case "Facil":
                velocidad1=1;
                break;
            case "Medio":
                velocidad1=2;
                break;
            case "Dificil":
                velocidad1=3;
                break;
            }
        }
    }//GEN-LAST:event_comboDificultadItemStateChanged

